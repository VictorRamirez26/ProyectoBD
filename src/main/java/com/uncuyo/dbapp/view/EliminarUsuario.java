        // TODO add your handling code here:
        String correoUsuario = txtCorreoUsuario.getText();
        if (controlador.findUser(correoUsuario)){
            Usuario usuario = controlador.getUsuario(correoUsuario);
            List<RegistroComida> listaRegistroComidas = controlador.getRegistroComidas(correoUsuario); 
            controlador.eliminarUsuario(usuario , listaRegistroComidas);
            JOptionPane.showMessageDialog(this, "El usuario se elimino correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "El correo ingresado no existe", "Error", JOptionPane.ERROR_MESSAGE);
        }
